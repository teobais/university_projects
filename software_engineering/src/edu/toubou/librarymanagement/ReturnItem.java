    private void addOneToTitlesAvailableItems()
    {
        String query = "UPDATE available_items_books SET AvailableItems=(AvailableItems+1) WHERE Name="
                + " '"+title.getTitleName()+"' ";
        
         try
        {
            pst = connection.prepareStatement(query);
            pst.execute();
            
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);       
        }
        finally{
            try{
                rs.close();
                pst.close();
                }
            catch(Exception e)
            {
            }
           }
    }
    
    private void removeOpenLending()
    {
        String query = "DELETE FROM open_lendings WHERE MemberId='"+memberIdJTextField.getText()+"' AND"
                + " Title='"+title.getTitleName()+"' AND ItemNo='"+title.getItemNo()+"' ";
        
         try
        {
            pst = connection.prepareStatement(query);
            pst.execute();
            
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);       
        }
        finally{
            try{
                rs.close();
                pst.close();
                }
            catch(Exception e)
            {
            }
           }
    }
    
    private void subtractOneFromMembersItemsBorrowed()
    {
        String query = "UPDATE members SET ItemsBorrowed=(ItemsBorrowed-1) WHERE MemberId='"+memberIdJTextField.getText()+"' ";
        String query2 = "UPDATE books SET IsBorrowed='0' WHERE Name='"+title.getTitleName()+"' AND ItemNo='"+title.getItemNo()+"' ";
         try
        {
            pst = connection.prepareStatement(query);
            pst = connection.prepareStatement(query2);
            pst.addBatch(query);
            pst.addBatch(query2);
            pst.executeBatch();
            JOptionPane.showMessageDialog(null,"All tables are up-to-date.\nYou can now return the Item to shelf.","Successful return",JOptionPane.INFORMATION_MESSAGE);
            
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);       
        }
        finally{
            try{
                rs.close();
                pst.close();
                }
            catch(Exception e)
            {
            }
           }
    }
    
