    private void returnJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnJButtonActionPerformed
        //if only noone of the 3 swing components is empty, we check member's lending rights
        if((!memberIdJTextField.getText().equals("")) && (!titleNameJTextField.getText().equals("")) && (!itemNoJTextField.getText().equals("")))
        {
            title = new Title();
            title.setTitleName(titleNameJTextField.getText());
            title.setItemNo(Integer.parseInt(itemNoJTextField.getText()));
            
            addOneToTitlesAvailableItems();
            removeOpenLending();
            subtractOneFromMembersItemsBorrowed();
            clearJComponents();
        }
        else
        JOptionPane.showMessageDialog(null,"Please fill all the required fields.","Invalid data",JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_returnJButtonActionPerformed

