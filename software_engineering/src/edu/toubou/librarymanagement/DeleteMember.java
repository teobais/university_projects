    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        memberId = jTextField1.getText();
        
        String query="select itemsBorrowed from members where MemberId=?";
        try
        {
            
            pst = connection.prepareStatement(query);
            pst.setString(1, memberId);
            rs = pst.executeQuery();
            while(rs.next())
            {
                if(rs.getInt("ItemsBorrowed") > 0)
                {
                    JOptionPane.showMessageDialog(null,"There are still some active lendings."
                            + "Member couldn't be deleted", "Unsubscribe Error Occured", JOptionPane.WARNING_MESSAGE);
                }
                else//there are not negative value for borrowed items ; thus, here we handle
                    //the case that the member with the exact id doesn;t owe any item
                {
                    String deleteMemberQuery = "delete from members where MemberId=?";
                    try
                    {
                        pst = connection.prepareStatement(deleteMemberQuery);
                        pst.setString(1, memberId);
                        
                        
                        int option =JOptionPane.showConfirmDialog(null,"Do you really"
                                + " want to delete this Member?","Are you sure?" ,JOptionPane.YES_OPTION);
                        if(option==JOptionPane.YES_OPTION)
                        {       pst.execute();
                         JOptionPane.showMessageDialog(null,"Member successfully deleted","Finished!" ,  JOptionPane.WARNING_MESSAGE);
                        jTextField1.setText("");}
                    }catch(SQLException e)
                    {
                        JOptionPane.showMessageDialog(null,e);

                    }
                    
                }
            }
        }catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);
            
        }
        finally{
            try{
                rs.close();
                pst.close();
                }
            catch(Exception e)
            {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

